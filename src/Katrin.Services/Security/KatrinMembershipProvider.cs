        public override bool ValidateUser(string username, string password)
        {
            string connectionString = ConfigurationManager.ConnectionStrings["Katrin"].ConnectionString;
            using (var connection = new SqlConnection(connectionString))
            {
                connection.Open();
                var commandText = "select Password from [User] where IsDeleted=0 and UserName = @UserName";
                var command = new SqlCommand(commandText, connection);
                command.Parameters.AddWithValue("UserName", username);
                var query = command.ExecuteScalar();
                if (query != null)
                {
                    string storedPassword = (string) query;
                    if (BCrypt.Net.BCrypt.Verify(password, storedPassword))
                        return true;
                }
                return false;
            }
        }
